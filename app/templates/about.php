        <section id="section2">
            <h3>Pourquoi développer un outil interne plutôt qu’utiliser une solution commerciale qui a déjà fait ses preuves ?</h3>
            <p class="desc desc-first">Au cours de leur expérience utilisateur, les CDA ont constaté que plusieurs fonctionnalités faisaient défaut sur la version gratuite de l’Outil. Ils décidèrent donc de développer Project Hive selon la méthode Kanban c'est-à-dire un tableau simple d'utilisation comportant des colonnes et des post-its.</p></br>
            <p class="desc">Project Hive devait permettre de combler les lacunes constatées avec notamment la possibilité de supprimer un tableau, de déplacer les colonnes (nos listes) et les post-its (nos cartes) sur toute la page. Il s’agissait également de laisser la possibilité à l’utilisateur de personnaliser l’interface pour d’une part, la rendre plus attrayante et d’autre part, améliorer la lisibilité des projets.</p></br>
            <p class="desc">Développer Project Hive à l’AFPA était donc une formidable opportunité pour tous : pour les stagiaires, acquérir ou accroître des compétences en lien direct avec leur programme de formation et pour l’AFPA, disposer rapidement d’un outil interne de gestion de projet intuitif (nécessitant peu ou aucune formation) à un coût défiant toute concurrence.</p></br>
            <p class="desc">C’est la CDA Women Team qui décidait de relever le défi en octobre 2021.</p>
        </section>

        <section id="section3">
            <h3>Et demain ?</h3>
