            <p class="desc">Pour consulter leur emploi du temps et partager des ressources, les stagiaires et leur formateur utilisaient un outil commercial collaboratif de planification que nous renommerons avec beaucoup d’originalité l’Outil. Néanmoins, très vite, est née l’idée de créer leur propre application, Project Hive.</p>
